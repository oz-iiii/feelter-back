// ÏãúÍ∞Ñ Í≥ÑÏÇ∞ Ìï®Ïàò
const getTimeAgo = (date: Date | string): string => {
  const now = new Date();
  const dateObj = typeof date === "string" ? new Date(date) : date;

  if (isNaN(dateObj.getTime())) {
    return "Ïïå Ïàò ÏóÜÏùå";
  }

  const diffInMs = now.getTime() - dateObj.getTime();
  const diffInMinutes = Math.floor(diffInMs / (1000 * 60));
  const diffInHours = Math.floor(diffInMs / (1000 * 60 * 60));
  const diffInDays = Math.floor(diffInMs / (1000 * 60 * 60 * 24));

  if (diffInMinutes < 1) return "Î∞©Í∏à";
  if (diffInMinutes < 60) return `${diffInMinutes}Î∂Ñ Ï†Ñ`;
  if (diffInHours < 24) return `${diffInHours}ÏãúÍ∞Ñ Ï†Ñ`;
  return `${diffInDays}Ïùº Ï†Ñ`;
};

// Ïª§ÎÆ§ÎãàÌã∞ Ìè¨Ïä§Ìä∏Î•º DiscussionÏúºÎ°ú Î≥ÄÌôòÌïòÎäî Ìï®Ïàò
const convertPostToDiscussion = (post: CommunityPost): Discussion => {
  const timeAgo = getTimeAgo(post.createdAt);

  return {
    id: post.id,
    type: "discussion",
    avatar: "üí≠",
    username: post.authorName,
    timestamp: timeAgo,
    title: post.title,
    preview: post.content,
    likes: post.likes,
    comments: post.comments,
    views: post.views,
    isActive: post.isActive,
    tags: post.tags,
    status: post.status,
  };
};

